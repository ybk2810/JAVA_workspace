package day01;

public class OperEx2 {

	public static void main(String[] args) {
		int a = 10;
		int b = 20;
		
		System.out.println("a + b = " + (a + b));
		System.out.println("a - b = " + (a - b));
		System.out.println("a * b = " + a * b);
		System.out.println("a / b = " + a / b);
		System.out.println("a % b = " + a % b);

		System.out.println("------------------------------");
		
		byte b1 = 10;
		byte b2 = 20;
		
		byte d = (byte)(b1 + b2);
		System.out.println("d : " + d);
	}

}
