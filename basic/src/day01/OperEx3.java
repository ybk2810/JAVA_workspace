package day01;

public class OperEx3 {
	public static void main(String[] args) {
		
		byte b1 = 10;
		byte b2 = 20;
		
		System.out.println("b1 >> 2 : " + (b1 >> 2));
		System.out.println("b1 << 2 : " + (b1 << 2));
		System.out.println("b2 >> 2 : " + (b2 >> 2));
		System.out.println("b2 << 4 : " + (b2 << 4));
		
	}
}
