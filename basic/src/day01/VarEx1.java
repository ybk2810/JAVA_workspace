package day01;

public class VarEx1 {
	public static void main(String[] args) {
		byte b;
		//define variable b, takes 8 bit size
		b = 10;
		//give b the value of 10
		System.out.println("variable b has the value of " + b);
		b = (byte) (b + 1);
		
		int k;
		//define variable k, takes 4 byte size
		k = 20;
		//give k the value of 20
		System.out.println("variable k has the value of " + k);
		//show the k value
		k = k + 1;
		//give k the value of k + 1
		System.out.println("variable k has the value of " + k);
		//show the k value
		
		short s = 120;
		//define variable s, takes 16 bit size
		
		s = 120;
		//give s the value of 120
		System.out.println("variable s has the value of " + s);
		//show the value of s
		
		
	}
}
